#include "../minitalk.h"

int	ft_atoi(const char *str)
{
	unsigned long long	r;
	int					i;
	int					n;

	i = 0;
	n = 1;
	r = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		if (r > ((9223372036854775807ULL - (str[i] - '0')) / 10) && (n == 1))
			return (-1);
		if (r > ((9223372036854775807ULL - (str[i] - '0')) / 10) && n == -1)
			return (0);
		i++;
	}
	return (r * n);
}